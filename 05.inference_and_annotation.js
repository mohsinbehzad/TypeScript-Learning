// Type Annotation: Manually specifying the data type.
// Type Inference: TypeScript automatically determines the data type.
// type annotation
var age = 22;
var myName = "Khan";
// type inference
var count = 10; // inferred as number
var city = "Islamabad"; // inferred as string
// Function example
function add(a, b) {
    return a + b; // return type inferred as number
}
console.log(add(5, 8));
